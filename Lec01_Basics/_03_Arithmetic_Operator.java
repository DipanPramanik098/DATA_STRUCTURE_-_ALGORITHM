package Lec01_Basics;

public class _03_Arithmetic_Operator {
    public static void main(String[] args) {
        int a = 7;
        int b = 9;
        int c = a + b;
        System.out.println(c);
        c = a - b;
        System.out.println(c);
        int d = a * b;
        System.out.println(d);
        d = a / b;
        System.out.println(d);
        d = a % b;
        System.out.println(d);
    }
}
